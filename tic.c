#include "tic.h"

//-----------------------------------
void TIC_Init(void)//инициализация таймера
{
	  TIMECON&=0x0;
	  INTVAL=0x1;
	  IEIP2 |= 0x04;
}
//-----------------------------------
void TIC_Start(void)//запуск таймера
{
	  TIMECON&=0x0;
	  INTVAL=0x1;
	 TIMECON=0x13;//00011011 -один период, прерывание 1 секунда	
	  

}
//-----------------------------------
void TIC_Stop(void)//останов таймера
{
	  TIMECON&=0x0; 	
}
//-----------------------------------
